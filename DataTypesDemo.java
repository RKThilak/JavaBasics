package com.edu;

public class DataTypesDemo {
public static void main(String[] args) {
	int a=1; // integer can hold only 4bytes
	long b=124l; //long can hold only 8bytes
	float c=10.1f;//float can hold only 4bytes
	double d=124.34;
	byte e= 127;
	char f='A';
	short g=12;
	boolean h=true;
	System.out.println(a+" " +b +" "+c +" "+d +" " +e +" " +f +" " +g +" " +h);
}
}
